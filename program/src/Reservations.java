public abstract class Reservations {
    protected int user_ID;
    protected String erk;
    protected String tav;
    protected int count;
    protected int room;


    public int getUser_ID() {
        return user_ID;
    }

    public String getErk() {
        return erk;
    }

    public String getTav() {
        return tav;
    }

    public int getCount() {
        return count;
    }

    public int getRoom() {
        return room;
    }
}
